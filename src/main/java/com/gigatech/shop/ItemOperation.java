package com.gigatech.shop;

// Enumeracja ItemOperation definiuje operacje, które można wykonać na przedmiotach w sklepie.
public enum ItemOperation {
    // INCREASE reprezentuje operację zwiększenia ilości przedmiotu w magazynie.
    INCREASE,
    // DECREASE reprezentuje operację zmniejszenia ilości przedmiotu w magazynie.
    DECREASE,
    // REMOVE reprezentuje operację usunięcia przedmiotu z magazynu.
    REMOVE
}

